// Contains unit conversion functions

export function kgToTons (value) {
  return value / 907.1847
}

export function msToMph (value) {
  return value * 2.2369
}

export function ms2ToMphs (value) {
  return value * 2.2369
}

export function mToFt (value) {
  return value * 3.2808
}

export function mphToMs (value) {
  return value / 2.2369
}
